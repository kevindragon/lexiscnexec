func Fetch() {
	rand.Seed(time.Now().UnixNano())

	f, err := os.Create("baike.csv")
	if err != nil {
		panic(err)
	}
	defer f.Close()

	f.WriteString("\xEF\xBB\xBF")

	w := csv.NewWriter(f)
	w.Write([]string{"#", "关键词", "法规", "别称"})

	for index, keyword := range keywords {
		fmt.Printf("Start with %s\n", keyword)

		entity, _ := GetRelatedLaws(keyword)

		fmt.Printf("Got %d entities", len(entity.Entities))
		if entity.Alias != "" {
			fmt.Printf(" and alias %s\n", entity.Alias)
		} else {
			fmt.Printf("\n")
		}

		if len(entity.Entities) == 0 {
			w.Write([]string{"1", keyword, "", entity.Alias})
		} else {
			for i, law := range entity.Entities {
				number := fmt.Sprintf("%d", i+1)
				if i < 1 {
					w.Write([]string{number, keyword, law, entity.Alias})
				} else {
					w.Write([]string{number, "", law, ""})
				}
			}
		}
		w.Write([]string{"", "", "", ""})

		random := rand.Intn(5) + 5

		fmt.Printf("Sleep %d seconds\n", random)

		time.Sleep(time.Second * time.Duration(random))

		if index > 1 {
			break
		}
	}
	w.Flush()
}
